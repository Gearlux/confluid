def test_baked_lookup_covers_class_and_engine_unions_it_without_source(
    pkg_factory: Callable[[str], Path], monkeypatch: pytest.MonkeyPatch
) -> None:
    warnings_seen = _capture_engine_warnings(monkeypatch)
    pkg_dir = pkg_factory("bakepkg_frozen")
    mod = importlib.import_module("bakepkg_frozen.mod")
    bake_broadcast_attrs(["bakepkg_frozen"])
    _freeze_package(pkg_dir)

    # Premise: the frozen simulation is real — the live scan is blind now.
    assert init_source_available(mod.BakedTrainer.__dict__["__init__"]) is False
    # The baked table covers the class (and its in-package base).
    assert baked_init_attrs(mod.BakedTrainer) == ("loss_fn", "model")
    assert baked_init_attrs(mod.EmptyInit) == ()

    attrs = engine_module._get_post_init_attrs(mod.BakedTrainer)
    assert {"loss_fn", "model"}.issubset(attrs)
    assert warnings_seen == []  # covered -> the cannot-scan warning stays quiet


def test_end_to_end_broadcast_from_baked_table_without_source(
    pkg_factory: Callable[[str], Path], monkeypatch: pytest.MonkeyPatch
) -> None:
    warnings_seen = _capture_engine_warnings(monkeypatch)
    pkg_dir = pkg_factory("bakepkg_e2e")
    importlib.import_module("bakepkg_e2e.mod")  # registers BakedTrainer
    bake_broadcast_attrs(["bakepkg_e2e"])
    _freeze_package(pkg_dir)

    cfg = load("loss_fn: custom_loss\ntrainer: !class:BakedTrainer\n  model: my_model\n")
    trainer: Any = flow(cfg["trainer"])
    assert trainer.model == "my_model"
    assert trainer.loss_fn == "custom_loss"  # broadcast landed via the baked names
    assert warnings_seen == []


def test_unbaked_sourceless_class_still_warns_and_mentions_bake(
    pkg_factory: Callable[[str], Path], monkeypatch: pytest.MonkeyPatch
) -> None:
    warnings_seen = _capture_engine_warnings(monkeypatch)
    pkg_dir = pkg_factory("bakepkg_unbaked")
    mod = importlib.import_module("bakepkg_unbaked.mod")
    _freeze_package(pkg_dir)  # NO bake step ran

    attrs = engine_module._get_post_init_attrs(mod.BakedTrainer)
    assert "loss_fn" not in attrs  # the divergence the warning is about
    mine = [msg for msg in warnings_seen if "bakepkg_unbaked" in msg]
    assert len(mine) == 1
    assert "confluid-bake" in mine[0]
    assert "broadcast_attrs" in mine[0]


def test_stale_baked_names_do_not_override_a_readable_scan(pkg_factory: Callable[[str], Path]) -> None:
    # Source AVAILABLE: the live scan is authoritative; a (stale) baked entry
    # for a class whose scan found names is never consulted.
    pkg_factory("bakepkg_stale")
    mod = importlib.import_module("bakepkg_stale.mod")
    doctored: Dict[str, Any] = {"bakepkg_stale.mod.BakedTrainer": ("ghost_slot",)}
    baked_module_path("bakepkg_stale").write_text(f"BROADCAST_ATTRS = {doctored!r}\n")
    introspect_module._baked_tables.pop("bakepkg_stale", None)

    engine_module._post_init_attrs_cache.clear()
    attrs = engine_module._get_post_init_attrs(mod.BakedTrainer)
    assert "loss_fn" in attrs  # live scan
    assert "ghost_slot" not in attrs  # stale bake ignored while source exists


def test_baked_lookup_returns_none_without_bake_module() -> None:
    assert baked_init_attrs(dict) is None  # builtins ship no bake table


if __name__ == "__main__":
    pytest.main([__file__])

def test_scan_package_collects_every_own_init_class(pkg_factory: Callable[[str], Path]) -> None:
    pkg_factory("bakepkg_scan")
    table = scan_package("bakepkg_scan")
    assert table["bakepkg_scan.mod.BakedTrainer"] == ("loss_fn", "model")  # sorted
    assert table["bakepkg_scan.mod.PlainBase"] == ("base_slot",)  # non-configurable baked too
    assert table["bakepkg_scan.mod.EmptyInit"] == ()  # empty entry is meaningful
    assert "bakepkg_scan.mod.NoInit" not in table  # no own __init__ -> no entry


def test_bake_writes_provenance_headed_importable_module(pkg_factory: Callable[[str], Path]) -> None:
    pkg_dir = pkg_factory("bakepkg_write")
    summary = bake_broadcast_attrs(["bakepkg_write"])
    baked_path = pkg_dir / "_confluid_baked.py"
    assert baked_path.exists()
    assert summary["bakepkg_write"]["changed"] is True
    assert summary["bakepkg_write"]["path"] == str(baked_path)

    content = baked_path.read_text()
    assert "AUTO-GENERATED FILE — DO NOT EDIT BY HAND" in content
    assert "Generated by: python -m confluid.bake bakepkg_write" in content

    baked = importlib.import_module("bakepkg_write._confluid_baked")
    assert baked.BROADCAST_ATTRS == scan_package("bakepkg_write")


def test_bake_is_idempotent_and_check_detects_drift(pkg_factory: Callable[[str], Path]) -> None:
    pkg_factory("bakepkg_check")
    bake_broadcast_attrs(["bakepkg_check"])
    # Fresh re-bake and check both see no drift.
    assert bake_broadcast_attrs(["bakepkg_check"])["bakepkg_check"]["changed"] is False
    assert bake_broadcast_attrs(["bakepkg_check"], check=True)["bakepkg_check"]["changed"] is False

    # Corrupt the table: check reports drift WITHOUT rewriting.
    baked_path = baked_module_path("bakepkg_check")
    baked_path.write_text("BROADCAST_ATTRS = {}\n")
    assert bake_broadcast_attrs(["bakepkg_check"], check=True)["bakepkg_check"]["changed"] is True
    assert baked_path.read_text() == "BROADCAST_ATTRS = {}\n"  # check mode never writes


def test_main_cli_writes_and_check_exits_nonzero_on_drift(
    pkg_factory: Callable[[str], Path], capsys: pytest.CaptureFixture[str]
) -> None:
    pkg_factory("bakepkg_cli")
    assert main(["bakepkg_cli"]) == 0
    assert "bakepkg_cli" in capsys.readouterr().out
    assert main(["bakepkg_cli", "--check"]) == 0

    baked_module_path("bakepkg_cli").write_text("BROADCAST_ATTRS = {}\n")
    assert main(["bakepkg_cli", "--check"]) == 1
    assert "out of date" in capsys.readouterr().err


def test_render_is_deterministic(pkg_factory: Callable[[str], Path]) -> None:
    pkg_factory("bakepkg_det")
    table = scan_package("bakepkg_det")
    assert render_baked_module("bakepkg_det", table) == render_baked_module(
        "bakepkg_det", dict(reversed(table.items()))
    )



"""``confluid.bake`` — build-time AST bake of ``__init__`` body-slot names.

The bake step runs the same AST scan as the runtime engine while source still
exists and emits ``<package>/_confluid_baked.py``; at runtime the engine unions
the baked names in whenever the live scan comes up empty (compiled / frozen /
zip deployments). The "frozen" condition is simulated for real here: import the
package, bake, DELETE the ``.py`` source, clear ``linecache`` — after that
``inspect.getsource`` raises exactly as in a frozen app.

Warnings are asserted via the SimpleNamespace-collector pattern (loggair is not
caplog-capturable — see the AGENTS logging mandate).
"""

import importlib
import linecache
import sys
from pathlib import Path
from types import SimpleNamespace
from typing import Any, Callable, Dict, List

import pytest

import confluid.engine as engine_module
import confluid.introspect as introspect_module
from confluid import flow, load
from confluid.bake import bake_broadcast_attrs, baked_module_path, main, render_baked_module, scan_package
from confluid.introspect import baked_init_attrs, init_source_available

_PKG_MODULE_SOURCE = '''\
from confluid import configurable


@configurable
class BakedTrainer:
    """Minimal-ctor class whose body slots the bake must capture."""

    def __init__(self, model: str = "default_model") -> None:
        self.model = model
        self.loss_fn = "default_loss"


class PlainBase:
    """NOT @configurable — in-package base classes are baked too (MRO parity)."""

    def __init__(self) -> None:
        self.base_slot = 1


class EmptyInit:
    """Empty body — bakes an EMPTY entry ("scanned, nothing found")."""

    def __init__(self) -> None:
        pass


class NoInit:
    """No own __init__ — no bake entry (the engine skips these too)."""
'''


@pytest.fixture
def pkg_factory(tmp_path: Path, monkeypatch: pytest.MonkeyPatch) -> Any:
    """Create importable throwaway packages under tmp_path; clean caches after.

    Package names must be unique per test (sys.modules and the baked-table
    cache are process-global); each test passes its own name.
    """
    monkeypatch.syspath_prepend(str(tmp_path))
    created: List[str] = []

    def make(name: str) -> Path:
        pkg_dir = tmp_path / name
        pkg_dir.mkdir()
        (pkg_dir / "__init__.py").write_text("")
        (pkg_dir / "mod.py").write_text(_PKG_MODULE_SOURCE)
        importlib.invalidate_caches()
        created.append(name)
        return pkg_dir

    yield make

    for name in created:
        for mod_name in [m for m in sys.modules if m == name or m.startswith(f"{name}.")]:
            sys.modules.pop(mod_name, None)
        introspect_module._baked_tables.pop(name, None)
    linecache.clearcache()


def _capture_engine_warnings(monkeypatch: pytest.MonkeyPatch) -> List[str]:
    seen: List[str] = []
    monkeypatch.setattr(
        engine_module,
        "logger",
        SimpleNamespace(
            warning=lambda msg: seen.append(msg),
            trace=lambda msg: None,
            debug=lambda msg: None,
            info=lambda msg: None,
            error=lambda msg: None,
        ),
    )
    return seen


def _freeze_package(pkg_dir: Path) -> None:
    """Simulate a frozen deployment: source gone, linecache cold, caches cleared."""
    (pkg_dir / "mod.py").unlink()
    linecache.clearcache()
    engine_module._post_init_attrs_cache.clear()
    engine_module._acceptable_keys_cache.clear()


